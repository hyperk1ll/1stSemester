# Deskripsi: membuat definisi, spesifikasi, dan realisasi dari sebuah predikat yang menerima sebuah karakter dan bernilai benar jika karakter tersebut adalah huruf 'A'

# Definisi dan spesifikasi dari fungsi Apakah huruf A bernama IsAnA?(C) adalah:
# IsAnA : character --> boolean
#   IsAnA(C) benar jika c adalah karakter(huruf) 'A'

# Realisasi
def IsAnA (c):
    return c == 'A'

# Aplikasi

# IsAnA (1)
# IsAnA (0)
# IsAnA (-1)

print (IsAnA (1))
print (IsAnA (0))
print (IsAnA (-1))
